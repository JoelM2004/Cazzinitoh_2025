class User {
  final String id;
  final String name;
  final String nameTag;
  final int age;
  final String email;
  final String profilePictureUrl;

  User({
    required this.id,
    required this.name,
    required this.nameTag,
    required this.age,
    required this.email,
    required this.profilePictureUrl,
  });
}
